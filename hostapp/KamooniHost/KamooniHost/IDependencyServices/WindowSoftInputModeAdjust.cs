namespace KamooniHost.IDependencyServices
{
    public enum WindowSoftInputModeAdjust
    {
        //
        // Summary:
        //     Indicates that the content of the control will pan, possibly off of the screen,
        //     to accommodate the input interface.
        Pan = 0,

        //
        // Summary:
        //     Indicates that the content of the control will resize to accommodate the input
        //     interface.
        Resize = 1,

        //
        // Summary:
        //     Indicates that the behavior of the control for oversized content is not specified.
        Unspecified = 2
    }
}